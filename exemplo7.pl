# Operadores de strings: promover a modificação em cadeias de caracteres

# Operadores de incremento: representado por ++, vão agir incrementado o ultimo caractere de uma cadeira de caracteres

$var1 = "b";
$var1++;

# print $var1;

# Operador de concatenação: representando por um unico caractere de ponto ( . ) -> promove a união de duas cadeias de caracteres

$var1 = "João";
